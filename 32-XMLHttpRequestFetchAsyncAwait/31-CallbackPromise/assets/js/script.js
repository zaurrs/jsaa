    // Məhsulu wishlist-ə əlavə edib silmək üçün funksiyanı işlədirik
    function toggleWishlist(productId, heart) {
        if (!currentUser) {
            toast("Zəhmət olmasa daxil olun");
            return;
        }

        if (!Array.isArray(currentUser.wishList)) {
            currentUser.wishList = [];
        }

        if (currentUser.wishList.some(item => item.id === productId)) {
            // Məhsulu wishlist-dən silirik
            currentUser.wishList = currentUser.wishList.filter(item => item.id !== productId);
            heart.classList.remove("fas");
            heart.classList.add("far");
            toast("Məhsul wishlist-dən silindi");
        } else {
            // Məhsulu wishlist-ə əlavə edirik
            let product = products.find(product => product.id === productId);
            
            if (product) {
                currentUser.wishList.push(product);
                heart.classList.remove("far");
                heart.classList.add("fas");
                toast("Məhsul wishlist-ə əlavə edildi");
            } else {
                toast("Məhsul tapılmadı");
            }
        }

        // Dəyişiklikləri `users` massivində saxlayırıq
        let userIndex = users.findIndex((user) => user.id === currentUser.id);
        users[userIndex] = currentUser;
        localStorage.setItem("users", JSON.stringify(users));
    }

    function addBasket(productId){
        if(!currentUser){
            toast("Zəhmət olmasa daxil olun");
            setTimeout(()=>{
                window.location.href="login.html"
            }, 1000)
        }
        let userIndex = users.findIndex(user=> user.id===currentUser.id)
        if(userIndex ===-1){
            toast("user not found")
            return
        }
        let basket = currentUser.basket || []
        let exsistProduct=basket.find((product)=>product.id===productId)
        if(exsistProduct){
            exsistProduct.count++
        }else{
            let product = products.find(product=>product.id===productId)
            if(product){
                currentUser.basket.push( {...product, count: 1})
            }
        }
        users[userIndex]=currentUser    
        localStorage.setItem("users", JSON.stringify(users))
        updateBasket()
    }


    function updateBasket(){
        let basketElement = document.querySelector(".basketIcon sup")
        let basketCount = currentUser?.basket.reduce((acc, product)=> acc+product.count, 0)
        basketElement.textContent=basketCount
    }
    // Toast mesajını göstərmək üçün funksiyanı işlədirik
    function toast(message) {
        Toastify({
            text: message,
            duration: 2000,
            gravity: "top",
            position: "left",
            style: {
                background: "linear-gradient(to right, #00b09b, #96c93d)"
            },
        }).showToast();
    }
updateBasket()
